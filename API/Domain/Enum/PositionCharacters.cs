namespace API.Domain.Enum;

public enum PositionCharacters
{
    MAIN,           // Personagem principal
    SUPPORTING,     // Personagem secundário
    ANTAGONIST,     // Antagonista
    PROTAGONIST,    // Protagonista (geralmente é o personagem principal)
    SIDEKICK,       // Ajudante ou "escudeiro" do personagem principal
    VILLAIN,        // Vilão (um tipo de antagonista, geralmente malvado)
    HERO,           // Herói (um personagem que é o salvador ou figura heroica)
    COMIC_RELIEF,   // Personagem de alívio cômico
    LOVE_INTEREST,  // Interesse romântico
    MENTOR,         // Mentor ou guia do personagem principal
    RIVAL,          // Rival do protagonista
    FOIL,           // Personagem que destaca as qualidades do protagonista por contraste
    BACKGROUND,     // Personagem de fundo, sem muito foco na trama
    MINOR           // Personagem menor, mas que pode ter importância em momentos específicos
}
