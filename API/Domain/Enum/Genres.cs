namespace API.Domain.Enum;

public enum Genres
{
    Action,         // Ação
    Adventure,      // Aventura
    Comedy,         // Comédia
    Drama,          // Drama
    Fantasy,        // Fantasia
    Magic,          // Magia
    SliceOfLife,    // Slice of Life (histórias do cotidiano)
    Romance,        // Romance
    Horror,         // Terror
    SciFi,          // Ficção Científica
    Thriller,       // Suspense
    Mystery,        // Mistério
    Sports,         // Esportes
    Mecha,          // Mecha (robôs gigantes)
    Music,          // Música
    Supernatural,   // Sobrenatural
    School,         // Escolar
    Shounen,        // Gênero voltado para o público jovem masculino
    Shoujo,         // Gênero voltado para o público jovem feminino
