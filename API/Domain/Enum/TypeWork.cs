namespace API.Domain.Enum;

public enum TypeWork
{
    Manhwa,       // Quadrinhos coreanos
    WebNovel,     // Romances publicados online
    Manga,        // Quadrinhos japoneses
    LightNovel,   // Romances ilustrados, populares no Japão
    Novel,        // Romance tradicional (não necessariamente ilustrado)
    Doujinshi,    // Obras criadas por fãs (geralmente baseadas em mangas ou animes)
    VisualNovel,  // Jogos interativos com foco em história e escolhas
    Comic,         // Quadrinhos ocidentais
    Novelization, // Adaptação literária de filmes, séries ou jogos
}