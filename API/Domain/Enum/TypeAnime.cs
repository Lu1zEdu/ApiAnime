namespace API.Domain.Enum;

public enum TypeAnime
{
    TV,            // Anime exibido na televisão
    Movie,         // Filme de anime
    OVA,           // Anime lançado diretamente em vídeo (Original Video Animation)
    ONA,           // Anime lançado diretamente online (Original Net Animation)
    Special,       // Episódio especial ou evento (geralmente fora da trama principal)
    Web,           // Anime transmitido via plataforma de streaming (ex: YouTube, Netflix)
    Theatrical,    // Anime exibido nos cinemas
    Pilot,         // Episódio piloto, geralmente utilizado para testar a viabilidade do anime
    MusicVideo,    // Anime relacionado a videoclipes musicais (ex: AMVs)
}
